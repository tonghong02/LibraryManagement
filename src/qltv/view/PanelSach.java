    public PanelSach() {
        try {
            initComponents();

            con = DBConnect.getConnection();
            st = con.createStatement();
            
            rs = st.executeQuery("SELECT COUNT(MaSach) FROM Sach");
            if(rs.next()){
                lbTongMaSach.setText(String.valueOf(rs.getInt(1)));
            }
            
            rs = st.executeQuery("SELECT SUM(SoLuong) FROM Sach;");
            if(rs.next()){
                lbTongSoSach.setText(String.valueOf(rs.getInt(1)));
            }
        } catch (SQLException ex) {
            Logger.getLogger(PanelSach.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        updateNXB();
        updateTheLoai();
        updateTacGia();
    }

    public void updateNXB() {
        conn.updateTable(tbNXB, "SELECT NXB, COUNT(NXB) as SoLuong FROM Sach  GROUP BY NXB;");

    }

    public void updateTheLoai() {
        conn.updateTable(tbTheLoai, "SELECT TheLoai, COUNT(TheLoai) as SoLuong FROM Sach GROUP BY TheLoai;");
    }

    public void updateTacGia() {
        conn.updateTable(tbTacGia, "SELECT TacGia, COUNT(TacGia) as SoLuong FROM Sach GROUP BY TacGia;");
    }

